			<p>
				The <a href="http://igem.org/Main_Page">International Genetically Engineered Machine</a> (iGEM) Foundation is an independent, non-profit organization dedicated to education and competition, the advancement of synthetic biology, and the development of an open community and collaboration.
			</p>
			<p>
				The <a href="http://igem.org/Competition">iGEM World Jamboree</a> is an annual, world wide, synthetic biology event aimed at undergraduate university students, as well as high school and graduate students. 280 multidisciplinary teams from all around the world work throughout the summer to build genetically engineered systems using <a href="http://parts.igem.org/Help:Parts">standard biological parts</a> called BioBricks. iGEM teams work inside and outside the lab, creating sophisticated projects that strive to create a positive contribution to their communities and the world.
			</p>
